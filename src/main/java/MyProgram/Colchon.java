    private void btnIngresoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIngresoMouseClicked
        if (!HaySaldo()) {
            JOptionPane.showMessageDialog(this, "No tiene saldo disponible", "Sin saldo", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        try {
            String input = JOptionPane.showInputDialog(this, "Digita la Cantidad de dinero que vas a ingresar", "Ingresar Plata", JOptionPane.PLAIN_MESSAGE);
            if (input == null) return;
        
            int valor = Integer.parseInt(input);
            if (valor <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor que cero", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        
            if (!ValidarIngreso(valor)) {
                JOptionPane.showMessageDialog(this, "No tienes suficiente saldo para este ingreso", "Saldo insuficiente", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        Ingreso(valor);
        cargarDatos();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un n√∫mero v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_btnIngresoMouseClicked

    private void btnRetiroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRetiroMouseClicked
        if (!HayColchon()) {
            JOptionPane.showMessageDialog(this, "No tiene saldo en el colch√≥n", "Sin saldo", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        try {
            String input = JOptionPane.showInputDialog(this, "Digita la Cantidad de dinero que vas a retirar", "Retirar Plata", JOptionPane.PLAIN_MESSAGE);
            if (input == null) return; // Si el usuario cancela
        
            int valor = Integer.parseInt(input);
            if (valor <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor que cero", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        
            if (!ValidarRetiro(valor)) {
                JOptionPane.showMessageDialog(this, "No tienes esa cantidad en el colch√≥n", "Fondos insuficientes", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Retiro(valor);
            cargarDatos();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un n√∫mero v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_btnRetiroMouseClicked

